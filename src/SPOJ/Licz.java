package SPOJ;

public class Licz {
    private char litera;
    private int ile;

    public Licz() {
    }

    public Licz(char litera) {
        this.litera = litera;
        this.ile = 1;
    }

    public char getLitera() {
        return litera;
    }

    public void setLitera(char litera) {
        this.litera = litera;
    }

    public int getIle() {
        return ile;
    }

    public void setIle(int ile) {
        this.ile = ile;
    }



}