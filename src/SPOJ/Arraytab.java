package SPOJ;

public class Arraytab {
    private char litera;
    private int value;

    public Arraytab(char litera, int value) {
        this.litera = litera;
        this.value = value;
    }

    public char getLitera() {
        return litera;
    }

    public void setLitera(char litera) {
        this.litera = litera;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
